def multiply_until_exceeding_limit(limit=5000):
    while True:
        try:
            num1 = float(input("Enter the first number: "))
            num2 = float(input("Enter the second number: "))
            product = num1 * num2
            print(f"The product of {num1} and {num2} is {product}")
            if product > limit:
                print(f"Product exceeded {limit}. Stopping the program.")
                break
        except ValueError:
            print("Invalid input. Please enter numeric values.")
multiply_until_exceeding_limit()
