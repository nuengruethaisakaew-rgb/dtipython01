#โดยให้ป้อนทะเบียนรถบรรทุก และน้ำหนักรถบรรทุกทางแป้นพิมพ์
print('=======================')
print('โปรแกรมตรวจสอบน้ำหนักรถบรรทุก')
print('=======================')
ทะเบียนรถ = input('ทะเบียนรถ : ')
น้ำหนักรถบรรทุก = float (input('น้ำหนักรถบรรทุก : '))
print('=======================')
if น้ำหนักรถบรรทุก > 1000 : # อย่าลืมใส่ Colon
    print(f'รถทะเบียน {ทะเบียนรถ} ของคุณ ไมผ่านเกณฑ์')
    print(f'น้ำหนักรถไม่ผ่านเกณฑ์...')
else : 
    print(f'รถทะเบียน {ทะเบียนรถ} ของคุณ ผ่านเกณฑ์')