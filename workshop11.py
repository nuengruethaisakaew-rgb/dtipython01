# สร้างโปรแกรมคำนวณค่า BMI โดยรับ ชื่อ ค่าส่วนสูง(เซ็นติเมตร) และนำหนัก (กิโลกรัม) ทางแป้นพิมพ์ แล้วแสดงผลค่า BMI ที่คำนวณได้ทางหน้าจอ โดย
# BMI =  น้ำหนัก หารด้วย ส่วนสูงยกกำลัง2
 
# --------------------------------
#    โปรแกรม BMI CALCULATOR
# --------------------------------
# ป้อนชื่อ: <input>
# ป้อนน้ำหนัก: <input>
# ป้อนส่วนสูง: <input>
# --------------------------------
# คุณ <output> หนัก <output> kg. สูง <output> m.
# มีค่า BMI เป็น <output>
# --------------------------------
# --------------------------------
#    โปรแกรม BMI CALCULATOR
# --------------------------------

# รับข้อมูลจากผู้ใช้
name = input("ป้อนชื่อ: ")
weight = float(input("ป้อนน้ำหนัก (kg): "))
height_cm = float(input("ป้อนส่วนสูง (cm): "))

print("--------------------------------")

# แปลงส่วนสูงจาก cm -> m
height_m = height_cm / 100

# คำนวณค่า BMI
bmi = weight / (height_m ** 2)

# แปลผลค่า BMI
if bmi < 18.5:
    status = "ผอม"
elif bmi < 23:
    status = "ปกติ"
elif bmi < 25:
    status = "ท้วม"
elif bmi < 30:
    status = "อ้วนระดับ 1"
else:
    status = "อ้วนระดับ 2"

# แสดงผล
print(f"คุณ {name} หนัก {weight:.1f} kg. สูง {height_m:.2f} m.")
print(f"มีค่า BMI เป็น {bmi:.2f} ({status})")

print("--------------------------------")